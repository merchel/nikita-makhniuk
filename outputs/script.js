function setSelectOptions(select, values) {
  if (!select) return;
  const oldIndex = select.selectedIndex > -1 ? select.selectedIndex : 0;
  select.innerHTML = values.map((value) => `<option value="${value}">${value}</option>`).join("");
  select.selectedIndex = Math.min(oldIndex, values.length - 1);
}

function fillSelects() {
  const localized = options[currentLang];
  setSelectOptions(document.getElementById("calcService"), localized.service);
  setSelectOptions(document.getElementById("calcDamage"), localized.damage);
  setSelectOptions(document.getElementById("calcArea"), localized.area);
  setSelectOptions(document.getElementById("calcPaint"), localized.paint);
  setSelectOptions(document.getElementById("contactService"), localized.service);
}

function calculateEstimate() {
  const serviceIndex = document.getElementById("calcService")?.selectedIndex || 0;
  const damageIndex = document.getElementById("calcDamage")?.selectedIndex || 0;
  const areaIndex = document.getElementById("calcArea")?.selectedIndex || 0;
  const paintIndex = document.getElementById("calcPaint")?.selectedIndex || 0;

  let price = ["$250-$700", "$700-$1,800", "$1,800-$5,000+"][damageIndex];
  let time = ["1-2 days", "2-5 days", "5-10 days"][damageIndex];

  if (currentLang === "ru") {
    time = ["1-2 дня", "2-5 дней", "5-10 дней"][damageIndex];
  }

  if (serviceIndex === 4 || areaIndex === 5) {
    price = "$3,000-$8,000+";
    time = currentLang === "ru" ? "5-10 дней" : "5-10 days";
  }

  if (serviceIndex === 5 || areaIndex === 6) {
    price = "$2,000-$8,000+";
    time = currentLang === "ru" ? "5-14 дней" : "5-14 days";
  }

  if (serviceIndex === 2 && damageIndex === 0) {
    price = "$450-$1,100";
  }

  if (serviceIndex === 3 && damageIndex === 0) {
    price = "$250-$900";
  }

  if (serviceIndex === 6 && damageIndex < 2) {
    price = "$350-$1,500";
    time = currentLang === "ru" ? "1-4 дня" : "1-4 days";
  }

  if (paintIndex === 0 && damageIndex < 2 && serviceIndex !== 4 && serviceIndex !== 5) {
    price = serviceIndex === 3 ? "$350-$1,100" : price;
  }

  const priceEl = document.getElementById("estimatePrice");
  const timeEl = document.getElementById("estimateTime");
  if (priceEl) priceEl.textContent = price;
  if (timeEl) timeEl.textContent = time;
}

function openServiceModal(index) {
  const service = services[index];
  if (!service) return;

  const item = service[currentLang];
  const modal = document.getElementById("serviceModal");

  document.getElementById("serviceModalImg").src = service.photo;
  document.getElementById("serviceModalImg").alt = item.title;
  document.getElementById("serviceModalTitle").textContent = item.title;
  document.getElementById("serviceModalText").textContent = item.detail;
  document.getElementById("serviceModalList").innerHTML = item.bullets
    .map((bullet) => `<li>${bullet}</li>`)
    .join("") + `<li>${text("time")}: ${item.time}</li>`;

  modal.classList.add("open");
  document.body.classList.add("modal-open");
}

function openWorkModal(index) {
  const work = workExamples[index];
  if (!work) return;

  const item = work[currentLang];
  const modal = document.getElementById("workModal");

  document.getElementById("workBefore").src = work.before;
  document.getElementById("workAfter").src = work.after;
  document.getElementById("workTitleModal").textContent = item.title;
  document.getElementById("workDescription").textContent = item.text;
  document.getElementById("workTime").textContent = `${text("time")}: ${item.time}`;
  document.getElementById("workPrice").textContent = `${text("price")}: ${item.price}`;

  modal.classList.add("open");
  document.body.classList.add("modal-open");
}

function closeModals() {
  document.querySelectorAll(".modal").forEach((modal) => modal.classList.remove("open"));
  document.body.classList.remove("modal-open");
}

document.addEventListener("click", (event) => {
  const navButton = event.target.closest(".nav-link[data-page]");
  const serviceButton = event.target.closest("[data-service-index]");
  const workCard = event.target.closest("[data-work-index]");
  const modalClose = event.target.closest(".modal-close");
  const modalBackdrop = event.target.classList.contains("modal") ? event.target : null;

  if (serviceButton) {
    openServiceModal(Number(serviceButton.dataset.serviceIndex));
    return;
  }

  if (workCard) {
    openWorkModal(Number(workCard.dataset.workIndex));
    return;
  }

  if (modalClose || modalBackdrop) {
    closeModals();
    return;
  }

  if (navButton) {
    setPage(navButton.dataset.page);
  }
});

document.addEventListener("keydown", (event) => {
  if (event.key === "Escape") closeModals();

  if ((event.key === "Enter" || event.key === " ") && event.target.matches("[data-work-index]")) {
    event.preventDefault();
    openWorkModal(Number(event.target.dataset.workIndex));
  }
});

